import { Effect } from "effect";
import { Database } from "../../effect/services/Database";
import { DatabaseError, QuotaError, ValidationError } from "../../effect/errors";
import { collectionTable, currentUserIdSql } from "../../infra/db/schema";
import { toCamelCase } from "@contfu/svc-core";
import { incrementCount, checkQuota } from "../../infra/nats/quota-kv";
import { pack } from "msgpackr";
import { mapToBackendCollection } from "./mapToBackendCollection";

export interface CreateCollectionInput {
  displayName: string;
  name?: string;
  includeRef?: boolean;
  connectionId?: number;
  ref?: string;
}

const camelCasePattern = /^[a-z][a-zA-Z0-9]*$/;

/**
 * Create a new Collection for the current user.
 * The ID is auto-generated by the database.
 */
export const createCollection = (userId: number, input: CreateCollectionInput) =>
  Effect.gen(function* () {
    const { db } = yield* Database;

    const name = input.name ?? toCamelCase(input.displayName);

    if (!camelCasePattern.test(name)) {
      yield* Effect.fail(
        new ValidationError({
          field: "name",
          message: `Collection name "${name}" must be a camelCase identifier (e.g. "blogPosts")`,
        }),
      );
    }

    const quota = yield* Effect.promise(() => checkQuota(userId, "collections"));
    if (!quota.allowed) {
      yield* Effect.fail(
        new QuotaError({
          resource: "collections",
          current: quota.current,
          max: quota.max,
        }),
      );
    }

    const [inserted] = yield* Effect.tryPromise({
      try: () =>
        db
          .insert(collectionTable)
          .values({
            userId: currentUserIdSql,
            displayName: input.displayName,
            name,
            includeRef: input.includeRef ?? true,
            schema: pack({}),
            connectionId: input.connectionId ?? null,
            ref: input.ref ? Buffer.from(input.ref, "utf-8") : null,
          })
          .returning(),
      catch: (e) => new DatabaseError({ cause: e }),
    });

    yield* Effect.promise(() => incrementCount(userId, "collections"));

    return mapToBackendCollection(inserted, 0, 0);
  }).pipe(Effect.withSpan("collections.create", { attributes: { userId } }));
