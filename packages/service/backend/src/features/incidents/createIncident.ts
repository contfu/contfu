import { Effect } from "effect";
import { and, eq } from "drizzle-orm";
import { pack, unpack } from "msgpackr";
import { IncidentType, type SchemaIncompatibleDetails } from "@contfu/svc-core";
import { Database } from "../../effect/services/Database";
import { DatabaseError } from "../../effect/errors";
import { incidentTable, flowTable } from "../../infra/db/schema";

export interface CreateIncidentInput {
  flowId: number;
  type: IncidentType;
  message: string;
  details?: SchemaIncompatibleDetails | Record<string, unknown>;
}

export interface IncidentResult {
  id: number;
  userId: number;
  flowId: number;
  type: IncidentType;
  message: string;
  details: Record<string, unknown> | null;
  resolved: boolean;
  createdAt: Date;
  resolvedAt: Date | null;
}

/**
 * Create a new incident for a flow.
 * The ID is auto-generated by the database.
 */
export const createIncident = (userId: number, input: CreateIncidentInput) =>
  Effect.gen(function* () {
    const { db } = yield* Database;

    const [flow] = yield* Effect.tryPromise({
      try: () =>
        db
          .select({ id: flowTable.id })
          .from(flowTable)
          .where(and(eq(flowTable.id, input.flowId), eq(flowTable.userId, userId)))
          .limit(1),
      catch: (e) => new DatabaseError({ cause: e }),
    });

    if (!flow) {
      throw new Error("Flow not found");
    }

    const [inserted] = yield* Effect.tryPromise({
      try: () =>
        db
          .insert(incidentTable)
          .values({
            userId,
            flowId: input.flowId,
            type: input.type,
            message: input.message,
            details: input.details ? pack(input.details) : null,
          })
          .returning(),
      catch: (e) => new DatabaseError({ cause: e }),
    });

    return {
      id: inserted.id,
      userId: inserted.userId,
      flowId: inserted.flowId,
      type: Number(inserted.type) as IncidentType,
      message: inserted.message,
      details: inserted.details ? (unpack(inserted.details) as Record<string, unknown>) : null,
      resolved: inserted.resolved,
      createdAt: inserted.createdAt,
      resolvedAt: inserted.resolvedAt,
    } satisfies IncidentResult;
  }).pipe(Effect.withSpan("incidents.create", { attributes: { userId, flowId: input.flowId } }));
